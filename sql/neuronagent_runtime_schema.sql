-- ============================================================================
-- agent_runs: first-class record of a complete agent execution
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.agent_runs (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    agent_id UUID NOT NULL REFERENCES neurondb_agent.agents(id) ON DELETE CASCADE,
    session_id UUID NOT NULL REFERENCES neurondb_agent.sessions(id) ON DELETE CASCADE,
    task_input TEXT NOT NULL,
    task_metadata JSONB DEFAULT '{}',
    state TEXT NOT NULL DEFAULT 'created'
        CHECK (state IN (
            'created', 'queued', 'planning', 'executing',
            'awaiting_tool', 'executing_tool', 'evaluating_result',
            'updating_memory', 'retrying', 'blocked',
            'completed', 'failed', 'canceled'
        )),
    plan_id UUID,
    current_step_index INTEGER DEFAULT 0,
    total_steps INTEGER,
    retry_count INTEGER DEFAULT 0,
    final_answer TEXT,
    error_class TEXT,
    error_detail JSONB,
    tokens_used JSONB,
    cost_estimate NUMERIC(10,6),
    started_at TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    org_id UUID,
    checkpoint JSONB
);
COMMENT ON TABLE neurondb_agent.agent_runs IS 'First-class record of a complete agent execution (state machine)';

CREATE INDEX IF NOT EXISTS idx_agent_runs_agent_created ON neurondb_agent.agent_runs(agent_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_agent_runs_session ON neurondb_agent.agent_runs(session_id);
CREATE INDEX IF NOT EXISTS idx_agent_runs_state ON neurondb_agent.agent_runs(state);
CREATE INDEX IF NOT EXISTS idx_agent_runs_org_created ON neurondb_agent.agent_runs(org_id, created_at DESC) WHERE org_id IS NOT NULL;

-- ============================================================================
-- agent_plans: structured plans generated by the planner
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.agent_plans (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID NOT NULL REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    version INTEGER NOT NULL DEFAULT 1,
    steps JSONB NOT NULL DEFAULT '[]',
    reasoning TEXT,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.agent_plans IS 'Structured plans generated by the planner; steps array contains action, tool, payload, dependencies, can_parallel, retry_strategy';

CREATE INDEX IF NOT EXISTS idx_agent_plans_run_version ON neurondb_agent.agent_plans(run_id, version DESC);

-- FK from agent_runs to agent_plans (circular dependency resolved after agent_plans exists)
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.table_constraints
        WHERE constraint_schema = 'neurondb_agent'
          AND table_name = 'agent_runs'
          AND constraint_name = 'agent_runs_plan_id_fkey'
    ) THEN
        ALTER TABLE neurondb_agent.agent_runs
        ADD CONSTRAINT agent_runs_plan_id_fkey
        FOREIGN KEY (plan_id) REFERENCES neurondb_agent.agent_plans(id) ON DELETE SET NULL;
    END IF;
END $$;

-- ============================================================================
-- agent_steps: individual step execution records within a run
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.agent_steps (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID NOT NULL REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_index INTEGER NOT NULL,
    plan_step_ref INTEGER,
    state TEXT NOT NULL DEFAULT 'pending'
        CHECK (state IN ('pending', 'executing', 'evaluating', 'completed', 'failed', 'retrying', 'skipped')),
    action_type TEXT NOT NULL
        CHECK (action_type IN ('tool_call', 'model_call', 'memory_write', 'plan_update', 'human_approval')),
    action_input JSONB DEFAULT '{}',
    action_output JSONB,
    evaluation JSONB,
    duration_ms INTEGER,
    retry_count INTEGER DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    completed_at TIMESTAMPTZ,
    UNIQUE (run_id, step_index)
);
COMMENT ON TABLE neurondb_agent.agent_steps IS 'Individual step execution records within a run; evaluation contains status, confidence, failure_class, repair_action';

CREATE INDEX IF NOT EXISTS idx_agent_steps_run_index ON neurondb_agent.agent_steps(run_id, step_index);
CREATE INDEX IF NOT EXISTS idx_agent_steps_state ON neurondb_agent.agent_steps(state);

-- ============================================================================
-- run_tool_invocations: every tool call with full input/output and timing
-- (Named run_tool_invocations to avoid conflict with audit tool_invocations.)
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.run_tool_invocations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_id UUID REFERENCES neurondb_agent.agent_steps(id) ON DELETE SET NULL,
    tool_name TEXT NOT NULL REFERENCES neurondb_agent.tools(name) ON DELETE RESTRICT,
    tool_version INTEGER,
    input_args JSONB NOT NULL DEFAULT '{}',
    input_valid BOOLEAN,
    output_result JSONB,
    output_valid BOOLEAN,
    status TEXT NOT NULL
        CHECK (status IN ('success', 'error', 'timeout', 'circuit_open')),
    error_code TEXT,
    error_message TEXT,
    retryable BOOLEAN,
    idempotency_key TEXT,
    duration_ms INTEGER,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.run_tool_invocations IS 'Runtime tool invocations per run/step with normalized envelope and validation';

CREATE INDEX IF NOT EXISTS idx_run_tool_invocations_run ON neurondb_agent.run_tool_invocations(run_id);
CREATE INDEX IF NOT EXISTS idx_run_tool_invocations_tool_created ON neurondb_agent.run_tool_invocations(tool_name, created_at DESC);
CREATE UNIQUE INDEX IF NOT EXISTS idx_run_tool_invocations_idempotency
    ON neurondb_agent.run_tool_invocations(idempotency_key) WHERE idempotency_key IS NOT NULL;

-- ============================================================================
-- model_calls: every LLM invocation with prompt metadata and usage
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.model_calls (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_id UUID REFERENCES neurondb_agent.agent_steps(id) ON DELETE SET NULL,
    model_name TEXT NOT NULL,
    model_provider TEXT,
    prompt_hash TEXT,
    prompt_sections JSONB,
    prompt_tokens INTEGER,
    completion_tokens INTEGER,
    total_tokens INTEGER,
    cost_estimate NUMERIC(10,6),
    latency_ms INTEGER,
    finish_reason TEXT,
    routing_reason TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.model_calls IS 'Every LLM invocation with prompt metadata and token usage';

CREATE INDEX IF NOT EXISTS idx_model_calls_run ON neurondb_agent.model_calls(run_id);
CREATE INDEX IF NOT EXISTS idx_model_calls_model_created ON neurondb_agent.model_calls(model_name, created_at DESC);

-- ============================================================================
-- execution_traces: fine-grained state transition log for the runtime FSM
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.execution_traces (
    id BIGSERIAL PRIMARY KEY,
    run_id UUID NOT NULL REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_id UUID REFERENCES neurondb_agent.agent_steps(id) ON DELETE SET NULL,
    from_state TEXT,
    to_state TEXT NOT NULL,
    trigger TEXT,
    metadata JSONB DEFAULT '{}',
    duration_ms INTEGER,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.execution_traces IS 'Fine-grained state transition log for the runtime state machine';

CREATE INDEX IF NOT EXISTS idx_execution_traces_run_created ON neurondb_agent.execution_traces(run_id, created_at);

-- ============================================================================
-- retrieval_events and context_builds (Phase 2 tables, minimal placeholders)
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.retrieval_events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_id UUID REFERENCES neurondb_agent.agent_steps(id) ON DELETE SET NULL,
    query_text TEXT,
    query_embedding_hash TEXT,
    source_tier TEXT CHECK (source_tier IN ('working', 'episodic', 'semantic', 'knowledge')),
    results_count INTEGER,
    results_used INTEGER,
    top_score REAL,
    budget_tokens INTEGER,
    tokens_used INTEGER,
    metadata JSONB DEFAULT '{}',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.retrieval_events IS 'Audit trail for every memory/knowledge retrieval decision';

CREATE INDEX IF NOT EXISTS idx_retrieval_events_run ON neurondb_agent.retrieval_events(run_id);
CREATE INDEX IF NOT EXISTS idx_retrieval_events_tier_created ON neurondb_agent.retrieval_events(source_tier, created_at DESC) WHERE source_tier IS NOT NULL;

CREATE TABLE IF NOT EXISTS neurondb_agent.context_builds (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    run_id UUID REFERENCES neurondb_agent.agent_runs(id) ON DELETE CASCADE,
    step_id UUID REFERENCES neurondb_agent.agent_steps(id) ON DELETE SET NULL,
    model_call_id UUID REFERENCES neurondb_agent.model_calls(id) ON DELETE SET NULL,
    total_budget_tokens INTEGER,
    sections JSONB DEFAULT '[]',
    prompt_hash TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.context_builds IS 'Record of each context assembly for reproducibility';

CREATE INDEX IF NOT EXISTS idx_context_builds_run ON neurondb_agent.context_builds(run_id);

-- ============================================================================
-- tools.result_schema: optional JSON Schema for tool output validation
-- ============================================================================
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'neurondb_agent' AND table_name = 'tools' AND column_name = 'result_schema'
    ) THEN
        ALTER TABLE neurondb_agent.tools ADD COLUMN result_schema JSONB;
    END IF;
END $$;
COMMENT ON COLUMN neurondb_agent.tools.result_schema IS 'Optional JSON Schema for validating tool output (data field in result envelope)';

-- ============================================================================
-- repo_index: for repo analysis mode (chunked code/docs with embeddings)
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.repo_index (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    agent_id UUID NOT NULL REFERENCES neurondb_agent.agents(id) ON DELETE CASCADE,
    repo_path TEXT NOT NULL,
    file_path TEXT NOT NULL,
    file_hash TEXT,
    language TEXT,
    chunk_type TEXT CHECK (chunk_type IN ('function', 'class', 'module', 'file', 'section')),
    chunk_name TEXT,
    chunk_content TEXT,
    embedding vector(768),
    summary TEXT,
    token_count INTEGER,
    line_start INTEGER,
    line_end INTEGER,
    metadata JSONB DEFAULT '{}',
    indexed_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.repo_index IS 'Repo analysis mode: indexed file chunks with embeddings';

CREATE INDEX IF NOT EXISTS idx_repo_index_agent_repo_file ON neurondb_agent.repo_index(agent_id, repo_path, file_path);

-- ============================================================================
-- repo_dependencies: import/dependency graph for repo analysis
-- ============================================================================
CREATE TABLE IF NOT EXISTS neurondb_agent.repo_dependencies (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    agent_id UUID NOT NULL REFERENCES neurondb_agent.agents(id) ON DELETE CASCADE,
    repo_path TEXT NOT NULL,
    source_file TEXT NOT NULL,
    target_file TEXT NOT NULL,
    dependency_type TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE neurondb_agent.repo_dependencies IS 'Import/dependency graph for repo analysis mode';

CREATE INDEX IF NOT EXISTS idx_repo_deps_agent_repo ON neurondb_agent.repo_dependencies(agent_id, repo_path);
